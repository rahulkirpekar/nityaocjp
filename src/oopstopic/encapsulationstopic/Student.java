package oopstopic.encapsulationstopic;
public class Student 
{
	private int rno;
	private String name;
	private int std;
	// Mutators--Setters Method
	public void setRno(int rno) 
	{
		this.rno=rno;
	}
	public void setName(String name) 
	{
		this.name=name;
	}
	public void setStd(int std) 
	{
		this.std=std;
	}
	// Accessors - Getters Methods
	public int getRno() 
	{
		return rno;
	}
	public String getName() 
	{
		return name;
	}
	public int getStd() 
	{
		return std;
	}
}
